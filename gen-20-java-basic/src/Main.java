// public : merupakan akses yang digunakan
// Main : identifier yang digunakan sesuai dengan nama file
public class Main {
    // void : merupakan keyword yang digunakan pada method yang tidak memiliki return value
    // main : nama method untuk menjalankan program
    public static void main(String[] args){
        System.out.println("Hello World");
        // system : package yang dimiliki oleh java yang dapat mendefinisikan beberapa fungsi
        // out : adalah fungsi mengeluarkan hasil
        // println : method yang digunakan untuk menampilkan hasil pada monitor
    }
}